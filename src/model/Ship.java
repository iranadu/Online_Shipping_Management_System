package model;

public class Ship {
	
	public int S_id;
	public String S_Name;
	public int S_Type;
	public String S_Captain_Name;
	public int S_Capacity;
	/**
	 * @return the s_id
	 */
	public int getS_id() {
		return S_id;
	}
	/**
	 * @param s_id the s_id to set
	 */
	public void setS_id(int s_id) {
		S_id = s_id;
	}
	/**
	 * @return the s_Name
	 */
	public String getS_Name() {
		return S_Name;
	}
	/**
	 * @param s_Name the s_Name to set
	 */
	public void setS_Name(String s_Name) {
		S_Name = s_Name;
	}
	/**
	 * @return the s_Type
	 */
	public int getS_Type() {
		return S_Type;
	}
	/**
	 * @param s_Type the s_Type to set
	 */
	public void setS_Type(int s_Type) {
		S_Type = s_Type;
	}
	/**
	 * @return the s_Captain_Name
	 */
	public String getS_Captain_Name() {
		return S_Captain_Name;
	}
	/**
	 * @param s_Captain_Name the s_Captain_Name to set
	 */
	public void setS_Captain_Name(String s_Captain_Name) {
		S_Captain_Name = s_Captain_Name;
	}
	/**
	 * @return the s_Capacity
	 */
	public int getS_Capacity() {
		return S_Capacity;
	}
	/**
	 * @param s_Capacity the s_Capacity to set
	 */
	public void setS_Capacity(int s_Capacity) {
		S_Capacity = s_Capacity;
	}
	
	

}
