package model;

public class Employee {

	public int Emp_id;
	public String Emp_Name;
	public int Emp_Telephone;
	public String Emp_Address;
	public int Emp_Salary;
	
	/**
	 * @return the emp_id
	 */
	public int getEmp_id() {
		return Emp_id;
	}
	/**
	 * @param emp_id the emp_id to set
	 */
	public void setEmp_id(int emp_id) {
		Emp_id = emp_id;
	}
	/**
	 * @return the emp_Name
	 */
	public String getEmp_Name() {
		return Emp_Name;
	}
	/**
	 * @param emp_Name the emp_Name to set
	 */
	public void setEmp_Name(String emp_Name) {
		Emp_Name = emp_Name;
	}
	/**
	 * @return the emp_Telephone
	 */
	public int getEmp_Telephone() {
		return Emp_Telephone;
	}
	/**
	 * @param emp_Telephone the emp_Telephone to set
	 */
	public void setEmp_Telephone(int emp_Telephone) {
		Emp_Telephone = emp_Telephone;
	}
	/**
	 * @return the emp_Address
	 */
	public String getEmp_Address() {
		return Emp_Address;
	}
	/**
	 * @param emp_Address the emp_Address to set
	 */
	public void setEmp_Address(String emp_Address) {
		Emp_Address = emp_Address;
	}
	/**
	 * @return the emp_Salary
	 */
	public int getEmp_Salary() {
		return Emp_Salary;
	}
	/**
	 * @param emp_Salary the emp_Salary to set
	 */
	public void setEmp_Salary(int emp_Salary) {
		Emp_Salary = emp_Salary;
	}
	
}
